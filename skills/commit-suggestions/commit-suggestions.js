import * as dotenv from "dotenv";
import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";
import { exec } from "child_process";
import { promisify } from "util";

dotenv.config();

const execPromise = promisify(exec);
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

export async function commitSuggestions(analysisResults, suggestionsText) {
  try {
    console.log(`\n💾 Committing code review suggestions to Git...\n`);

    const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
    const suggestionsFilename = `pr-suggestions-${analysisResults.pr_number}-${timestamp}.md`;
    const suggestionsPath = path.join(process.cwd(), 'suggestions', suggestionsFilename);

    // Create suggestions directory if it doesn't exist
    const suggestionsDir = path.join(process.cwd(), 'suggestions');
    if (!fs.existsSync(suggestionsDir)) {
      fs.mkdirSync(suggestionsDir, { recursive: true });
    }

    // Create suggestions file
    const suggestionsContent = `# Code Review Suggestions for PR #${analysisResults.pr_number}

**Repository:** ${analysisResults.repository}
**PR Title:** ${analysisResults.pr_title}
**PR Author:** ${analysisResults.pr_author}
**Analysis Date:** ${new Date().toISOString()}
**Files Analyzed:** ${analysisResults.files_analyzed}

## Summary
- **Total Issues:** ${analysisResults.summary.total_issues}
- **Critical:** ${analysisResults.summary.critical}
- **Warnings:** ${analysisResults.summary.warnings}
- **Suggestions:** ${analysisResults.summary.suggestions}

## Review

${suggestionsText}

## Issues Found

${analysisResults.issues.map((issue, idx) => `
### Issue ${idx + 1}: ${issue.issue}
- **File:** \`${issue.file}\`
- **Line:** ${issue.line}
- **Type:** ${issue.type}
- **Severity:** ${issue.severity}
- **Why:** ${issue.why}
- **Suggestion:** ${issue.suggestion}
`).join('\n')}

---
*Generated by pr-analyzer-agent on ${new Date().toISOString()}*
*This is an automated code review. Please review and apply suggestions as appropriate.*
`;

    fs.writeFileSync(suggestionsPath, suggestionsContent);
    console.log(`✅ Suggestions saved to: suggestions/${suggestionsFilename}`);

    // Git operations
    try {
      // Stage the file
      await execPromise(`git add suggestions/${suggestionsFilename}`);
      console.log('✅ File staged in Git');

      // Commit with message
      const commitMessage = `chore: code-review suggestions for PR #${analysisResults.pr_number}

Repository: ${analysisResults.repository}
Issues Found: ${analysisResults.summary.total_issues} (Critical: ${analysisResults.summary.critical}, Warnings: ${analysisResults.summary.warnings})
Files Analyzed: ${analysisResults.files_analyzed}

Reviewed by: pr-analyzer-agent
Timestamp: ${new Date().toISOString()}`;

      await execPromise(`git commit -m "${commitMessage.replace(/"/g, '\\"')}"`);
      console.log('✅ Changes committed to Git');

      return {
        status: "success",
        message: `Successfully committed suggestions for PR #${analysisResults.pr_number}`,
        file: `suggestions/${suggestionsFilename}`,
        commit_message: commitMessage,
        pr_number: analysisResults.pr_number,
        repository: analysisResults.repository,
        timestamp: new Date().toISOString()
      };

    } catch (gitError) {
      // If git operations fail, still save the file
      console.warn('⚠️ Git commit failed (might not be in a git repo), but suggestions file created');
      return {
        status: "partial",
        message: `Suggestions created but Git commit failed: ${gitError.message}`,
        file: `suggestions/${suggestionsFilename}`,
        pr_number: analysisResults.pr_number,
        timestamp: new Date().toISOString()
      };
    }

  } catch (error) {
    console.error(`❌ Error committing suggestions: ${error.message}`);
    throw error;
  }
}

